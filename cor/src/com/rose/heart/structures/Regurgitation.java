package com.rose.heart.structures;

public class Regurgitation {
	String grade;
	Double volume;
	Double venaCon;
	
	

	public Double getVolume() {
		return volume;
	}

	public void setVolume(Double volume) {
		this.volume = volume;
	}

	public Double getVenaCon() {
		return venaCon;
	}

	public void setVenaCon(Double venaCon) {
		this.venaCon = venaCon;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
}
